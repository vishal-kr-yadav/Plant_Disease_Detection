def replace(values):
    """Replace all '.' with '123456789'
    Input: A sudoku in dictionary form.
    Output: The resulting sudoku in dictionary form."""
    for i in values.keys():
        if values[i] == '.':
            assign_value(values, i, '123456789')
    return values


